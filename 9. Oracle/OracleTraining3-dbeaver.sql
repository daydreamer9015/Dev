-- 5-17 급여가 2000 이상 3000 이하인 사원 데이터를 조회하시오.
SELECT *
FROM emp
WHERE sal >= 2000 AND sal <= 3000;

-- 5-18
SELECT  * FROM emp WHERE sal BETWEEN 2000 AND 3000;

-- 5-19 sal가 2000에서 3000 사이 값이 아닌 사원 데이터를 조회하시오.
SELECT * FROM emp WHERE sal NOT BETWEEN 2000 AND 3000 ORDER BY sal DESC;

-- 5-20 이름에 대문자 S로 시작하는 사원 데이터를 조회하시오.
SELECT * FROM emp WHERE ENAME LIKE 'S%';

-- 5-21 사원 이름 두번째 글자가 L인 데이터를 조회하시오.
SELECT * FROM emp WHERE ENAME LIKE '_L%';

-- 5-22 사원 이름 중 AM 포함하는 데이터를 조회하시오.
SELECT * FROM emp WHERE ENAME LIKE '%AM%';

-- 5-23 사원 이름 중 AM 포함하지 않는 데이터를 조회하시오.
SELECT * FROM emp WHERE ENAME NOT LIKE '%AM%';

