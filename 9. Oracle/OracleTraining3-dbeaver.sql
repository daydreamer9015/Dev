SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP
MINUS
SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP WHERE DEPTNO = 10;

-- INTERSECT : 10번 부서 직원만
SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP
INTERSECT 
SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP WHERE DEPTNO = 10;

-- 이름이 M으로 시작하거나 N으로 끝나는 직원의 사번, 이름, 입사일을 조회하시오.
SELECT EMPNO, ENAME, HIREDATE FROM EMP WHERE ENAME LIKE 'M%' OR ENAME LIKE '%N' ORDER BY ENAME;

-- 직무가 SALESMAN이 아닌 직원 중에서 급여가 1500 이상 3000 이하인 직원의 이름, 직무, 급여, 연봉을 조회하시오.
SELECT ENAME, SAL, JOB, SAL * 12 + NVL(COMM, 0) AS ANNUAL_SAL FROM EMP WHERE JOB != 'SALESMAN' AND SAL BETWEEN 1500 AND 3000;

-- 관리자가 있는 직원 중 급여가 2000 이상인 직원의 이름, 관리자번호, 급여를 조회하고 관리자번호, 사원 이름 순으로 정렬하시오.
SELECT ENAME, MGR, SAL FROM EMP WHERE MGR IS NOT NULL AND SAL >= 2000 ORDER BY MGR, ENAME;

-- 부서 번호가 10과 30의 직원 중 급여가 1500 이상인 직원을 UNION을 사용하여 조회하시오.
-- 사번, 이름, 급여, 부서번호를 출력하고 부서번호 오름차순, 급여 내림차순 기준으로 정렬하시오.
SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP WHERE SAL >= 1500 AND DEPTNO = 10
UNION
SELECT EMPNO, ENAME, SAL, DEPTNO FROM EMP WHERE SAL >= 1500 AND DEPTNO = 30
ORDER BY DEPTNO, SAL DESC;