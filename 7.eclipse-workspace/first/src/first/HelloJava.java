package first;

/*
 * 1) 자바 언어는 기본적으로 대소문자를 구별함
 * 	  보라색은 예약어(키워드)이므로 변수명으로 사용 X
 * 2) 파일명과 클래스 이름이 같아야 함
 * 	- 이때 클래스는 public으로 선언되어야 함
 * 3) public static void main(String[] args)
 * 	- main method 라고 함
 * 	- 자바는 main(String[] args) 메서드를 찾아서 프로그램을 시작함
 * 	- main은 프로그램의 시작점이라고 이해하면 됨
 * 4) System.out.println("Hello World!");
 * 	- 값을 콘솔에 출력하는 기능
 * 	- 자바는 문자열을 사용할 때 ""(쌍따옴표)를 사용함
 * 	- 자바스크립트는 '', "" 둘 다 사용 가능
 * 	- ; : 자바는 세미콜론으로 문장 구분함
 * 		- 문장이 끝나면 세미콜론을 필수로 넣어줘야 함
 * 
 * * 괄호
 * 	1) 소괄호() : 함수
 * 	2) 중괄호{} : 블럭. 클래스, 메서드 다음
 * 	3) 대괄호[] : 배열
 */

public class HelloJava {
	public static void main(String[] args) {
		System.out.println("Hello World!");
		System.out.println("Hello World!");
		System.out.println("Hello World!");
		}
}
